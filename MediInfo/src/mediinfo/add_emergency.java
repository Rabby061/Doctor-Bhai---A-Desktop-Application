    public boolean validadd()
    {
        String hospital=jTextField2.getText();
        String location=jTextField1.getText();
        String contact=jTextField4.getText();
        if(hospital.equals(""))
        {
            JOptionPane.showMessageDialog(this,"Please enter hospital name");
            return false;
        }
        
        if(location.equals(""))
        {
            JOptionPane.showMessageDialog(this,"Please enter rent");
            return false;
        }
        if(contact.equals(""))
        {
            JOptionPane.showMessageDialog(this,"Please enter contact number");
            return false;
        }
        return true;
    }
    
    public boolean checkhospital()
    {
        String hospital=jTextField2.getText();
        boolean isexist=false;
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/mysql","root","12345");
            PreparedStatement pst=conn.prepareStatement("select * from emergency where hospital=?");
            pst.setString(1, hospital);
            ResultSet rs=pst.executeQuery();
            if(rs.next())
            {
                isexist=true;
            }
            else
            {
                isexist=false;
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        return isexist;
    }
    public void addemergency()
     {
        String hospital=jTextField2.getText();
        String location=jTextField1.getText();
        String contact=jTextField4.getText();
        
        try
        {
            Connection conn=javaconnect.getConnection();
            String sql="insert into emergency(hospital,location,contact_number) values(?,?,?)";
            PreparedStatement pst=conn.prepareStatement(sql);
            pst.setString(1,hospital);
            pst.setString(2,location);
            pst.setString(3,contact);
            
            int updaterow=pst.executeUpdate();
            if(updaterow>0)
            {
                JOptionPane.showMessageDialog(this,"Data added successfully");
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Invalid information");
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
    
    public void updateemergency()
    {
        String hospital=jTextField2.getText();
        String location=jTextField1.getText();
        String contact=jTextField4.getText();
        
        try
        {
            Connection conn=javaconnect.getConnection();
            String sql="UPDATE emergency SET location=?,contact_number=? where hospital=?";
            PreparedStatement pst=conn.prepareStatement(sql);
            pst.setString(1,location);
            pst.setString(2,contact);
            pst.setString(3,hospital);
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"Data Updated successfully");
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }

